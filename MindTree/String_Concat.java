package com.Mindtree.MindTree;

import java.util.Scanner;

public class String_Concat {
    public static void main(String[] args) {
        String a,b;
        Scanner sc = new Scanner(System.in);

        a= sc.nextLine();
        b= sc.nextLine();

        a= a+""+b;

        System.out.println(a);









    }
}
